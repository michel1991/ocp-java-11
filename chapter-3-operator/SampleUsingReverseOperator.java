public class SampleUsingReverseOperator{
  public static void main(String[] args){
       double zooTemperature = 1.21;
      System.out.println(zooTemperature);
      zooTemperature = -zooTemperature;
     System.out.println(zooTemperature);
     zooTemperature = -(-zooTemperature);
    System.out.println(zooTemperature);
  }
}
