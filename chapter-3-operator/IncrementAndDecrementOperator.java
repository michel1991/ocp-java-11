public class IncrementAndDecrementOperator{
  public static void main(String[] args){
     int parkAttendance = 0;
     System.out.println("initial value parkAttendance "+ parkAttendance);
     System.out.println("pre increment value "+ " "+  ++parkAttendance);
     System.out.println("Park attendance nothing operator "+ parkAttendance);
    System.out.println("post decrement "+ " " + parkAttendance--);
     System.out.println("Park Attendance nothing operator "+ " " + parkAttendance);
  }
}
